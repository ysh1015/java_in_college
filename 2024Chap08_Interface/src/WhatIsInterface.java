
public class WhatIsInterface {
	// 추상 클래스와 연관이 깊다.
	
	// 다형성이 이루어지기 위해서는 인터페이스가 일치해야한다.
	// 한마디로 규격 정리, 규격이 안맞으면 다형성을 위반, 그것을 정의해주기 위해서 인터페이스가 필요
	// 인터페이스는 두 객체를 연결하는 역할, 다형성 구현에 주된 기술
	
	/*
	 * 인터페이스 - 구현(implements)        ==         클래스 - 상속(extends)
	 * 개념은 똑같다
	 * 
	 * 인터페이스-구현 관계는 클래스-상속 관계와 달리 다중상속이 가능하다.(부모가 두개 이상도 상관없다.)
	 * 추상클래스와 인터페이스에서 default 메서드는 일반클래스에서의 일반 메서드와 동일
	 * 
	 * 시그니처(signature) : 수학용어르서 함수의 이름을 말할 때
	 * */
}
